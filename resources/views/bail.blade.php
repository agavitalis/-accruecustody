                                                        <p>The constitutional protections, as well as the
                                                            Bail Reform Act (a federal statute), give a
                                                            defendant the right to request lower bail when
                                                            the amount initially set by the court is too
                                                            high to pay. A defendant can request a hearing
                                                            to seek a bail reduction.</p>

                                                    </div>
                                                </div>
                                                <div class="vc_empty_space" style="height: 20px"><span
