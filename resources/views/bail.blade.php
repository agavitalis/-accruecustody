                                                <div class="wpb_text_column wpb_content_element ">
                                                    <div class="wpb_wrapper">
                                                        <p>At the hearing, the defendant can argue that the
                                                            initial bail set by the court is so high that it
                                                            is effectively a denial of bail and amounts to
                                                            pretrial detention in jail, even though the
                                                            defendant is not a flight risk or a threat to
                                                            the public. (18 USC § 3142 (c)(2).) Although the
                                                            court must take this argument into
                                                            consideration, it is not required to set bail at
                                                            a level that the defendant can easily pay.
                                                            Courts can set bail high enough “to induce a
                                                            defendant to go to great lengths to raise the
                                                            funds without violating” the constitution or the
                                                            Bail Reform Act. (U.S. v. Szot, 768 F.2d 159
                                                            (7thCir., 1985).) As long as the court’s actual
                                                            motive is not to just force the defendant to rot
                                                            in jail awaiting trial, the court can set bail
                                                            at any level it can justify.</p>

                                                    </div>
                                                </div>
