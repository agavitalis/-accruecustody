                                                        <p>Just as the defendant has the right to seek a
                                                            lower bail, the prosecution can request that the
                                                            court set a higher level of bail based on the
                                                            risk that the defendant will flee from the
                                                            jurisdiction or inflict harm upon a victim or
                                                            other members of the public. (18 USC § 3142
                                                            (f).) And, the court may hold a hearing to
                                                            inquire into the source of bail funds that it
                                                            suspects may be illegal (proceeds from drug
                                                            sales, for example). Bail is only one of the
                                                            conditions that a court may impose in order to
                                                            grant release of a defendant from jail pending
                                                            trial. Other conditions include travel
                                                            restrictions, relinquishing a passport, drug
                                                            testing, electronic monitoring devices (ankle
                                                            bracelets), house arrest, supervision and
                                                            reporting during release, and others.</p>

