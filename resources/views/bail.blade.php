                                                <div class="wpb_text_column wpb_content_element ">
                                                    <div class="wpb_wrapper">
                                                        <p>Once a defendant informs the court, through a
                                                            bail reduction request or otherwise, that she
                                                            cannot afford the bail set, the court must
                                                            specify the reason(s) that the amount set is “an
                                                            indispensable” condition of release from jail.
                                                            (U.S. v. Montececon-Zayas, 949 F.2d 548 (1st
                                                            Cir. 1991).) The court must set out its
                                                            rationale in writing. (Fed. Rule of App. Proced.
                                                            9.)</p>

