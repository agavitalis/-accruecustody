                                                <h3 id="commercials">Agreement for loan – terms</h3>
                                                <p>A typical loan agreement sets out the terms on which a
                                                    lender will provide financing for the borrower and the
                                                    parties should consider whether to include the following
                                                    terms:</p>
                                                <h4>Conditions Precedent</h4>
                                                <p>These are pre-funding conditions which a lender wants to
                                                    see satisfied before agreeing to release funds.</p>
                                                <h4>Interest</h4>
                                                <p>The parties should consider whether interest is payable
                                                    on the loan being made and if so what the rate should
                                                    be.  There can be FCA implications as explained below.
                                                </p>
                                                <h4>Repayment and pre-payment terms</h4>
                                                <p>The parties should consider how and when the loan is to
                                                    be repaid and whether the borrower is entitled to make
                                                    any voluntary prepayments.</p>
                                                <h4>Indemnities</h4>
                                                <p>A lender often requires protection in the form of
                                                    indemnities (which is a promise by the borrower to pay
                                                    to the lender on a pound-for-pound basis on a particular
                                                    type of loss arising). For example, if there is an event
                                                    of default (please see further comments below).</p>
                                                <h3>Representations or warranties under part of the
                                                    loan terms</h3>
                                                <p>Representations or warranties – these are statements of
                                                    current or past fact (or law) and operate to flush out
                                                    information at the start of the transaction. Lenders use
                                                    representations to limit their lending risk.</p>
                                                <h4>Financial covenants in corporate loans</h4>
                                                <p>The objective of financial covenants is to define in
                                                    financial terms the parameters within which a borrower
                                                    may operate its business. They provide:</p>
                                                <ul>
                                                    <li>an objective assessment of change to a borrower’s
                                                        financial position;</li>
                                                    <li>the means of monitoring the borrower’s financial
                                                        position on a regular basis;</li>
                                                    <li>an early warning of potential financial difficulty
                                                        for a borrower;</li>
                                                    <li>a means of imposing financial discipline on a
                                                        borrower.</li>
                                                </ul>
                                                <h4>Events of default</h4>
                                                <p>Typically, a lender does not have an inherent right to
                                                    demand early repayment of a loan. Therefore,
                                                    the facility agreement needs to specify circumstances or
                                                    events that, if they were to occur, would give a lender
                                                    that right. These circumstances or events are usually
                                                    called events of default and will vary for individual
                                                    transactions and will need to be tailored and negotiated
                                                    as appropriate. They are usually heavily negotiated.</p>
                                                <h3 id="security">Security for lending</h3>
                                                <p>Typically, a lender agrees to lend to a borrower if it is
                                                    provided with sufficient security for the loan. If
                                                    security is provided, the loan is known as a secured
                                                    loan and the loan can be secured against, for example,
                                                    property of a borrower (in the form of a legal charge),
                                                    or business and assets of the borrower (in the form of a
                                                    debenture), which then becomes a secured debt owed to
                                                    the lender.</p>
                                                <h3>Default under a loan agreement</h3>
                                                <p>If a borrower defaults on a loan and is unable to repay
                                                    it whether in full or in part, the lender may seek to
                                                    enforce the security that it has taken if these are
                                                    reserved as part of the investment terms.</p>
                                                <h2 id="alternatives">Alternatives to loans</h2>
                                                <p>You want to lend money to a business but you are not
                                                    convinced of its financial viability or stability, how
                                                    can you secure and protect your interests?</p>
                                                <p>There may be a number of alternatives to consider.</p>
                                                <h3><strong>Third party guarantee and indemnity</strong>
                                                </h3>
                                                <p>Third parties can also guarantee and indemnify lending,
                                                    where a third party (usually connected to the borrower,
                                                    a sister company, parent company or connected
                                                    individual) guarantees their personal assets and
                                                    promises to pay where the borrower fails.  Guarantees
                                                    and indemnities will impose restrictions on the
                                                    guarantor personally.</p>
                                                <h3><strong>Equitable charge over shares</strong></h3>
                                                <p>People sometimes overlook that it is possible to take a
                                                    charge over shares.  The charge can extend to any shares
                                                    be they in the borrower or in a director’s portfolio.  
                                                    This could be helpful for companies with assets such as
                                                    commercial property.</p>
                                                <h3><strong>Personal Guarantees</strong></h3>
                                                <p>Personal guarantees can achieve security in private
                                                    lending if the director/shareholder has personal assets
                                                    that can be secured against.</p>
